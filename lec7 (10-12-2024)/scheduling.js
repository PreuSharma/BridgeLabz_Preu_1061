/*
Scheduling in JavaScript refers to how functions are executed after a certain amount of time or repeatedly, 
using mechanisms like setTimeout, setInterval, and the requestAnimationFrame method. */

setTimeout(() => {
    console.log('This message is displayed after 2 seconds.');
  }, 2000);

  setInterval(() => {
    console.log('This message is displayed every 2 seconds.');
  }, 2000);
    

