/*Comparison Operators
Equality Operators:

== (Loose Equality): Compares values without checking types.
=== (Strict Equality): Compares values and types.
Inequality Operators:

!= (Loose Inequality): Compares values without checking types.
!== (Strict Inequality): Compares values and types.
Relational Operators:

<: Less than.
<=: Less than or equal to.
>: Greater than.
>=: Greater than or equal to.

*/

console.log(5 == "5");  // true (loose equality allows type conversion)
console.log(5 === "5"); // false (strict equality checks type)
console.log(true == 1); // true (1 is truthy, so values match)
console.log(true === 1); // false (type mismatch)


